export function getCurrentPeriod(): number {
  const today = new Date();
  const currentDateStr = today.toISOString().split('T')[0];
  
  for (const period of periods) {
    const startDate = new Date(period.start);
    const endDate = new Date(period.end);
    
    if (today >= startDate && today <= endDate) {
      return period.id;
    }
  }
  
  // Default to period 7 if current date is July 6, 2025
  return 7;
}

export function getPeriodById(id: number): Period | undefined {
  return periods.find(p => p.id === id);
}

export function isPastPeriod(periodId: number): boolean {
  const currentPeriod = getCurrentPeriod();
  return periodId < currentPeriod;
}
